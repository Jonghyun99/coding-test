WITH ECOLI_TITLE AS (
    SELECT ID, NTILE(4) OVER(ORDER BY SIZE_OF_COLONY DESC) AS ECOLI_NUM
    FROM ECOLI_DATA
)

SELECT ID, 
CASE 
    WHEN ECOLI_NUM = 1 THEN 'CRITICAL'
    WHEN ECOLI_NUM = 2 THEN 'HIGH'
    WHEN ECOLI_NUM = 3 THEN 'MEDIUM'
    WHEN ECOLI_NUM = 4 THEN 'LOW'
END AS COLONY_NAME
FROM ECOLI_TITLE
ORDER BY ID