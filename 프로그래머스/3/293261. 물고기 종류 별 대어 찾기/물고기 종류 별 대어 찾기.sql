WITH FISH_RNK AS (
    SELECT 
        ID,
        RANK() OVER(PARTITION BY FISH_TYPE ORDER BY LENGTH DESC) AS RNK,
        LENGTH,
        FISH_TYPE
    FROM FISH_INFO
)

SELECT A.ID, B.FISH_NAME, A.LENGTH
FROM FISH_RNK A
JOIN FISH_NAME_INFO B ON A.FISH_TYPE = B.FISH_TYPE
WHERE A.RNK=1
ORDER BY ID